# ═══════════════════════════════════════════════════════════════
# 从东方财富API获取股票列表
# ═══════════════════════════════════════════════════════════════

# 东财 API pz 单页最大约 200，超过需分页
EASTMONEY_PAGE_SIZE = 200

@st.cache_data(ttl=900, show_spinner=False)  # 交易日15分钟
def fetch_eastmoney_stock_list(market: str = "us", limit: int = 350) -> List[Tuple[str, str, str]]:
    """
    从东方财富行情中心获取股票列表（支持分页，东财 pz 单页最大约200）
    
    Args:
        market: "us" (美股) / "hk" (港股) / "cn" (A股)
        limit: 返回数量
        
    Returns:
        [(code, name, yf_code), ...]
    """
    try:
        url = "http://80.push2.eastmoney.com/api/qt/clist/get"
        page_size = EASTMONEY_PAGE_SIZE
        all_stocks = []
        pn = 1
        while len(all_stocks) < limit:
            time.sleep(0.6)
            pz = min(page_size, limit - len(all_stocks))
            if market == "us":
                params = {
                    "pn": pn,
                    "pz": pz,
                    "fs": "m:105,m:106,m:107",
                    "fields": "f12,f14,f20",
                    "ut": "bd1d9ddb04089700cf9c27f6f7426281"
                }
            elif market == "hk":
                params = {
                    "pn": pn,
                    "pz": pz,
                    "fs": "m:128",
                    "fields": "f12,f14,f20",
                    "ut": "bd1d9ddb04089700cf9c27f6f7426281"
                }
            elif market == "cn":
                params = {
                    "pn": pn,
                    "pz": pz,
                    "fs": "m:0+t:6,m:0+t:80,m:1+t:2,m:1+t:23",
                    "fields": "f12,f14,f20",
                    "ut": "bd1d9ddb04089700cf9c27f6f7426281"
                }
            else:
                return []
            response = requests.get(url, params=params, timeout=10, verify=False)
            if response.status_code != 200:
                break
            data = response.json()
            if not data.get('data') or not data['data'].get('diff'):
                break
            diff = data['data']['diff']
            diff_list = diff if isinstance(diff, list) else (list(diff.values()) if isinstance(diff, dict) else [])
            page_stocks = []
            for item in diff_list:
                if not isinstance(item, dict):
                    continue
                code = item.get('f12', '')
                name = item.get('f14', '')
                if code and name:
                    yf_code = to_yf_cn_code(code)
                    page_stocks.append((code, name, yf_code))
            if not page_stocks:
                break
            all_stocks.extend(page_stocks)
            if len(page_stocks) < pz:
                break
            pn += 1
            if len(all_stocks) >= limit:
                break
        result = all_stocks[:limit]
        logging.info(f"股票池获取成功 {market.upper()}: {len(result)}只")
        return result
    except Exception as e:
        logging.error(f"股票池API失败 {market.upper()}: {type(e).__name__}: {str(e)[:100]}")
        return []


