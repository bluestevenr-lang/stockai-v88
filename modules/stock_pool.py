def init_stock_pools() -> Tuple[List[Tuple[str, str, str]], List[Tuple[str, str, str]], List[Tuple[str, str, str]]]:
    """
    初始化股票池（云端 + 本地备用）
    
    安全策略：
    - 美股: 350只
    - 港股: 200只
    - A股: 250只
    - 总计: 800只（= 800安全线）
    
    Returns:
        (us_pool, hk_pool, cn_pool)
    """
    logging.info("开始初始化股票池（安全模式：总量800只 = 800安全线）...")
    
    # 1. 美股池（350只）
    us_pool = fetch_eastmoney_stock_list("us", TARGET_POOL_SIZE["US"])
    if not us_pool or len(us_pool) < 30:
        logging.warning("美股云端获取失败，使用备用池")
        us_pool = get_backup_us_pool()
    
    # 2. 港股池（200只）
    hk_pool = fetch_eastmoney_stock_list("hk", TARGET_POOL_SIZE["HK"])
    if not hk_pool or len(hk_pool) < 30:
        logging.warning("港股云端获取失败，使用备用池")
        hk_pool = get_backup_hk_pool()
    
    # 3. A股池（250只）
    cn_pool = fetch_eastmoney_stock_list("cn", TARGET_POOL_SIZE["CN"])
    if not cn_pool or len(cn_pool) < 30:
        logging.warning("A股云端获取失败，使用备用池")
        cn_pool = get_backup_cn_pool()
    
    # 确保数量达标（混合模式）
    if len(us_pool) < TARGET_POOL_SIZE["US"]:
        backup = get_backup_us_pool()
        us_pool.extend([s for s in backup if s not in us_pool])
        us_pool = us_pool[:TARGET_POOL_SIZE["US"]]
    
    if len(hk_pool) < TARGET_POOL_SIZE["HK"]:
        backup = get_backup_hk_pool()
        hk_pool.extend([s for s in backup if s not in hk_pool])
        hk_pool = hk_pool[:TARGET_POOL_SIZE["HK"]]
    
    if len(cn_pool) < TARGET_POOL_SIZE["CN"]:
        backup = get_backup_cn_pool()
        cn_pool.extend([s for s in backup if s not in cn_pool])
        cn_pool = cn_pool[:TARGET_POOL_SIZE["CN"]]
    
    total = len(us_pool) + len(hk_pool) + len(cn_pool)
    logging.info(f"股票池初始化完成: 美股{len(us_pool)}只 + 港股{len(hk_pool)}只 + A股{len(cn_pool)}只 = 总计{total}只")
    
    return us_pool, hk_pool, cn_pool
